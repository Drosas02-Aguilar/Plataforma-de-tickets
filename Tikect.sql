CREATE TABLE TICKETS(
IDTICKET NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
TITULO VARCHAR2(250) NOT NULL,
DESCRIPCION CLOB,

PRIORIDAD VARCHAR2(20) NOT NULL,
ESTADO VARCHAR2(20) NOT NULL,

CREADOID NUMBER NOT NULL,
ASIGNADOID NUMBER,

  FechaCreacion TIMESTAMP DEFAULT SYSTIMESTAMP,
  FECHAACTUALIZACION TIMESTAMP,

CONSTRAINT FK_TICKECT_CREADOR FOREIGN KEY(CREADOID)
REFERENCES USUARIOS(IDUSUARIO),
CONSTRAINT FK_TICKET_ASIGNADO FOREIGN KEY (ASIGNADOID)
REFERENCES USUARIOS(IDUSUARIO),

CONSTRAINT CHK_TICKET_PRIORIDAD CHECK (PRIORIDAD IN('BAJA','MEDIA','ALTA')),
CONSTRAINT CHK_TICKET_ESTADO CHECK (PRIORIDAD IN('ABIERTO','PROCESO','RESUELTO','CERRADO'))

);

CREATE TABLE Comentarios (
  IDCOMENTARIO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  TicketsId NUMBER NOT NULL,
  AutorId NUMBER NOT NULL,
  Mensaje CLOB NOT NULL,
  FechaCreacion TIMESTAMP DEFAULT SYSTIMESTAMP,

  CONSTRAINT FK_Comentario_Ticket FOREIGN KEY (TicketsId)
    REFERENCES Tickets(IDTICKET) ON DELETE CASCADE,

  CONSTRAINT FK_Comentario_Autor FOREIGN KEY (AutorId)
    REFERENCES Usuarios(idUsuario)
);

