CREATE TABLE USUARIOROLES(
IDUSUARIOS NUMBER NOT NULL,
IDROLES NUMBER NOT NULL,
CONSTRAINT PK_USUARIOROLES PRIMARY KEY (IDUSUARIOS,IDROLES),
CONSTRAINT FK_USAURIOroLES_USUARIO FOREIGN KEY (IDUSUARIOS)
REFERENCES USUARIOS(IDUSUARIO) ON DELETE CASCADE,
CONSTRAINT FK_USUARIOROLES_ROL FOREIGN KEY (IDROLES) 
